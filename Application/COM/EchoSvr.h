class TEchoServer : public TCOMProtocol
{
  protected:
    uint8_t        *m_pucTxBuf   = nullptr;
    TCOMBuffer     *m_pRxBuffer  = nullptr;
    uint32_t        m_uRecvBytes = 0;
  
  public:
    TEchoServer() : TCOMProtocol() {  }
    TEchoServer( TCOMSocket *pSocket ) : TCOMProtocol( pSocket ) {  }
      
    virtual void OnReceive(const void* pvMsg = nullptr, uint32_t uNumBytes = 0);
  
    // 定时事件
    // uTick的单位是1ms
    virtual void OnTick(uint32_t uTick);

  protected:
    // 完成初始化
    virtual void Init(void);
    
    // 释放空间
    virtual void Free(void);
};
