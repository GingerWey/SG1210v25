{
}
//-----------------------------------------------------------------------------
void TCOMProtocol::SetSocket(TCOMSocket *pSocket)
{
  
  if( pSocket != m_pSocket )
    {
    m_pSocket = pSocket;
    }
}
//-----------------------------------------------------------------------------
uint32_t TCOMProtocol::Write(const void *pvBuffer, uint32_t uNumBytes )
{
  
  uint32_t uRes;

  if( m_pSocket )
    uRes = m_pSocket->Write( pvBuffer, uNumBytes );
  else
    uRes = 0;
  
  return uRes;
}
