#endif
//-----------------------------------------------------------------------------
// 创建任务
void CreateHMITask()
{
  
#if osCMSIS >= 0x20000U
  static const osThreadAttr_t HMITask_attributes = 
    {
    .name = "HMITask",
    .stack_size = 2048 * 4,
    .priority = (osPriority_t) osPriorityNormal,
    };
  osThreadNew(taskHMI, NULL, &HMITask_attributes);
#else
  osThreadDef(mmiTask, taskHMI, osPriorityNormal, 0, 1024);
  osThreadCreate(osThread(mmiTask), NULL);
#endif
}
