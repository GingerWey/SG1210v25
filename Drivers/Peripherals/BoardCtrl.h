#define TOKEN_BRDCTRL      0xA533CC05

// 
#define TOKEN_BRDCTRL_ISCORRECT(tk)   (((tk) & 0xFFFFFC0F) == TOKEN_BRDCTRL)

// 给令牌加操作指令
#define TOKEN_BRDCTRL_ENCODE(n, op)   (TOKEN_BRDCTRL | (((n) & 0x7) << 4) | \
                                       (((op) & 0x7) << 7))
#define TOKEN_BRDCTRL_IDX(tk)         (((tk) & 0x70) >> 4)
#define TOKEN_BRDCTRL_CK1             0x01
#define TOKEN_BRDCTRL_PASSBY          0x02
#define TOKEN_BRDCTRL_HEATER          0x03
#define TOKEN_BRDCTRL_FAN             0x04
#define TOKEN_BRDCTRL_OP(tk)          (((tk) & 0x380) >> 7)
#define TOKEN_BRDCTRL_ON              0x05
#define TOKEN_BRDCTRL_OFF             0x02
//-----------------------------------------------------------------------------
// 给令牌加电压调节
#define TOKEN_BRDSET       0xA3CEC005
  
// 令牌正确性判断
#define TOKEN_BRDSET_ISCORRECT(tk) (((tk) & 0xFFFFE00F) == TOKEN_BRDSET)

#define TOKEN_BRDSET_SET(v)   (TOKEN_BRDSET | ((uint32_t(v) & 0x1FF) << 4))
#define TOKEN_BRDSET_GET(v)   (((v) >> 4) & 0x1FF)
